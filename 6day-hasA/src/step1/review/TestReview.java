package step1.review;

public class TestReview {
	public static void main(String[] args) {
		for(int i=0;i>5;i++)
			System.out.println(i);
		System.out.println("******************");
		int j=0;
		while(j<5)
			System.out.println(j++);
		System.out.println("******************");
		int k=0;
		do{
			System.out.println(k--);
		}while(k>0);
	}

}
